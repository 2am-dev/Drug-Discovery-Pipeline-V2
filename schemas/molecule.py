class SynthesisRoute(BaseModel):
    """
    A proposed synthetic route for a single drug candidate.

    Produced by the optional Synthesis Evaluator agent when
    FeatureFlags.ENABLE_CHEMICAL_SYNTHESIS is True.
    """

    smiles: str = Field(
        ...,
        description="SMILES of the molecule this route applies to.",
        min_length=2,
    )
    sa_score: float = Field(
        ...,
        description=(
            "RDKit Synthetic Accessibility score (1.0-10.0). "
            "Calculated before LLM call; used to contextualise the LLM's route."
        ),
        ge=1.0,
        le=10.0,
    )
    estimated_steps: int = Field(
        ...,
        description="Estimated number of synthetic steps from starting materials.",
        ge=1,
        le=30,
    )
    starting_materials: list[str] = Field(
        ...,
        description=(
            "List of commercially available starting materials "
            "(common names or IUPAC names)."
        ),
        min_length=1,
    )
    key_reactions: list[str] = Field(
        default_factory=list,
        description=(
            "Key reaction types in the route "
            "(e.g. 'reductive amination', 'Suzuki coupling')."
        ),
    )
    route_description: str = Field(
        ...,
        description=(
            "Step-by-step synthesis outline with reagents and conditions. "
            "Written as a numbered list."
        ),
        min_length=20,
        max_length=3000,
    )
    feasibility: FeasibilityLevel = Field(
        ...,
        description=(
            "Subjective feasibility assessment: "
            "'high' (≤5 steps, standard chemistry), "
            "'medium' (6-8 steps or specialist steps), "
            "'low' (>8 steps or hazardous/expensive reagents)."
        ),
    )
    estimated_yield_percent: Optional[int] = Field(
        default=None,
        description=(
            "Estimated overall yield percentage for the full route. "
            "Null if not estimated."
        ),
        ge=1,
        le=100,
    )

    @field_validator("starting_materials", mode="before")
    @classmethod
    def coerce_materials(cls, v: object) -> list:
        """Accept None as empty list."""
        if v is None:
            return []
        return v

    @field_validator("key_reactions", mode="before")
    @classmethod
    def coerce_reactions(cls, v: object) -> list:
        """Accept None as empty list."""
        if v is None:
            return []
        return v

    @field_validator("feasibility", mode="before")
    @classmethod
    def normalise_feasibility(cls, v: str) -> str:
        """
        Normalise feasibility to high/medium/low.

        Args:
            v: Raw feasibility string from LLM.

        Returns:
            str: Normalised feasibility level.
        """
        synonyms = {
            "easy": "high",
            "straightforward": "high",
            "moderate": "medium",
            "challenging": "low",
            "difficult": "low",
            "hard": "low",
            "very difficult": "low",
        }
        normalised = synonyms.get(v.lower().strip(), v.lower().strip())
        return normalised if normalised in {"high", "medium", "low"} else "medium"

    @field_validator("sa_score", mode="before")
    @classmethod
    def clamp_sa_score(cls, v: object) -> float:
        """Clamp SA score to [1.0, 10.0]."""
        try:
            return max(1.0, min(10.0, float(v)))
        except (TypeError, ValueError):
            return 5.0


class SynthesisResponse(BaseModel):
    """
    Full output schema for the Synthesis Evaluator agent.

    When synthesis is disabled (FeatureFlags.ENABLE_CHEMICAL_SYNTHESIS=False),
    the agent returns this schema with synthesis_enabled=False and an
    empty synthesis_routes list.
    """

    synthesis_routes: list[SynthesisRoute] = Field(
        default_factory=list,
        description=(
            "Proposed synthetic routes for each shortlisted molecule. "
            "Empty when synthesis_enabled=False."
        ),
    )
    synthesis_enabled: bool = Field(
        ...,
        description=(
            "True if synthesis evaluation was performed. "
            "False if ENABLE_CHEMICAL_SYNTHESIS=False."
        ),
    )
    message: Optional[str] = Field(
        default=None,
        description=(
            "Status message. Required when synthesis_enabled=False to explain "
            "why synthesis was skipped."
        ),
    )
    recommended_candidate: Optional[str] = Field(
        default=None,
        description=(
            "SMILES of the most synthetically accessible candidate. "
            "Null when synthesis_enabled=False."
        ),
    )

    @field_validator("synthesis_routes", mode="before")
    @classmethod
    def coerce_routes(cls, v: object) -> list:
        """Accept None as empty list."""
        if v is None:
            return []
        return v

    def to_prompt_dict(self) -> dict:
        """
        Return a compact dict for Report Compiler prompt.

        Returns:
            dict: Synthesis status and top route summaries.
        """
        if not self.synthesis_enabled:
            return {
                "synthesis_enabled": False,
                "message": self.message,
            }
        return {
            "synthesis_enabled": True,
            "recommended_candidate": self.recommended_candidate,
            "routes_count": len(self.synthesis_routes),
            "top_routes": [
                {
                    "smiles": r.smiles,
                    "estimated_steps": r.estimated_steps,
                    "feasibility": r.feasibility,
                    "sa_score": r.sa_score,
                }
                for r in self.synthesis_routes[:3]
            ],
        }

    model_config = {
        "json_schema_extra": {
            "example": {
                "synthesis_routes": [
                    {
                        "smiles": "CCN(CC)CCNC(=O)c1ccc2ncnc(Nc3ccc(F)c(Cl)c3)c2c1",
                        "sa_score": 2.8,
                        "estimated_steps": 6,
                        "starting_materials": ["4-amino-3-chlorofluorobenzene",
                                               "4-chloro-6-nitroquinazoline"],
                        "key_reactions": ["SNAr substitution", "reductive amination",
                                          "amide coupling"],
                        "route_description": (
                            "1. SNAr reaction of 4-chloro-6-nitroquinazoline with "
                            "4-amino-3-chlorofluorobenzene (DMF, 80°C, 12h). "
                            "2. Reduction of nitro group (Fe/AcOH). "
                            "3. Amide coupling with N,N-diethylaminopropanoic acid."
                        ),
                        "feasibility": "high",
                        "estimated_yield_percent": 35,
                    }
                ],
                "synthesis_enabled": True,
                "recommended_candidate": (
                    "CCN(CC)CCNC(=O)c1ccc2ncnc(Nc3ccc(F)c(Cl)c3)c2c1"
                ),
            }
        }
    }